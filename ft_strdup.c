#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*copy;
	size_t	slen;
	int		i;

	i = 0;
	slen = ft_strlen(s1);
	copy = malloc(sizeof(char) * (slen + 1));
	if (!copy)
		return (NULL);
	while (s1[i])
	{
		copy[i] = s1[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
