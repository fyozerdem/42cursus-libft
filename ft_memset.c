#include "libft.h"

void	*ft_memset(void *s, int c, size_t len)
{
	char	*s2;
	int		i;

	i = 0;
	s2 = (char *)s;
	while (len > 0)
	{
		s2[i++] = c;
		len--;
	}
	return (s);
}
